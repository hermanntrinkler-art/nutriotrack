const translations = { de, en, es, fr, it, pt, pl, nl } as const;

type TranslationKey = keyof typeof de;

interface I18nContextType {
  t: (key: TranslationKey, params?: Record<string, string | number>) => string;
  language: Language;
  setLanguage: (lang: Language) => void;
}

const I18nContext = createContext<I18nContextType | null>(null);

const LANG_CODES: Language[] = ['de', 'en', 'es', 'fr', 'it', 'pt', 'pl', 'nl'];

function detectLanguage(): Language {
  const browserLang = navigator.language.slice(0, 2) as Language;
  return LANG_CODES.includes(browserLang) ? browserLang : 'en';
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    const stored = localStorage.getItem('nutrilens-language') as Language | null;
    if (stored && LANG_CODES.includes(stored)) return stored;
    return detectLanguage();
  });

  const setLanguage = useCallback((lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('nutrilens-language', lang);
  }, []);

  const t = useCallback((key: TranslationKey, params?: Record<string, string | number>): string => {
    let text: string = (translations[language] as Record<string, string>)[key] || (translations.de as Record<string, string>)[key] || key;
    if (params) {
      Object.entries(params).forEach(([k, v]) => {
        text = text.replace(`{${k}}`, String(v));
      });
    }
    return text;
  }, [language]);

  return (
    <I18nContext.Provider value={{ t, language, setLanguage }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useTranslation() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useTranslation must be used within I18nProvider');
  return ctx;
}
